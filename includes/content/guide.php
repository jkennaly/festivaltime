<h2>Introduction to this guide</h2>

<p>
This guide is a brief walkthrough on how to approach this website. Trying to encapsulate Coachella within a website is obviously not even possible, but this website is intended to help you get the most out of Coachella.
</p>

<h2>Highlights</h2>

<p>
Some quick highlights on what the site has to offer:
<ul>
<li>A convenient way to rate, comment and share links to Coachella bands with your fellow Coachella Peoples.</li>
</li>Easy access to YouTube-click any band name, and it will take you to a list of all the info about the band. Click on the band name on the details page, and it automaticcally searches Youtube for that band.</li>
<li>View the bands that have the best and worst ratings. If you think Blur is not the lamest Coachella headliner ever, now is your chance to give them an undeserved 3 rating while screaming Woo Hoo! at the top of your lungs.</li>
<li>All comments and ratings will be easily visible during the show. (smartphone reuired)</li>
<li>During the show, with only two button clicks, you will be able to broadcast to everyone what bands you are going to see. No trying to type out text messages from the mosh pit at How To Destroy Angels! (smartphone required)</li>
<li>No app to install, no data is collected about you, other than what you type in, and even that is not associated to anything more than a username. No risk of any security breach.</li>
</ul>
</p>


<p>
This guide is broken up into two major sections, one for pregame and one for gametime. On this site, pregame refers to anything that happens before April 12, and gametime is the span between April 12-14. The gametime section will be fleshed out as it gets closer.
</p>

</div>

<div id="pregame">

<h2>How to use this website during pregame</h2>

<p>
During pregame, this site is oriented towards helping you discover new bands, and let others know what you are thinking about the bands you do know. The easiest way to check out new bands is run some filters, click on a band that sounds interesting, and then click the band name to pop up the band on youtube. Pick a video that looks interesting, and then read the comments others have left while you listen to the band. If you find a video that is really great or particularly appaling, and it as a link with a short description.
</p>

<div id="findnew">

<p>
If you want to find new bands based on what others are listening to, the best option is the filter for high rating and filter for low rating options. These will let you see what others love and what they hate, and throw in your two cents. To only see new bands that others like, click the bands that I have not rated box and the bands with a high rating box.
</p>

</div>

<div id="cuttingedge">

<p>
If you want to get out in front and help fill in information on some of the lesser known acts, use the "NO ONE" filters. This will help you find bands that have no information of the selected type, so you can be the first to comment on who The Stone Roses are.
</p>

</div>
